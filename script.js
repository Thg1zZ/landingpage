let lang='pt',theme='dark';

function toggleLang(){
  lang=lang==='pt'?'en':'pt';
  document.getElementById('langBtn').textContent=lang==='pt'?'EN':'PT';
  document.documentElement.lang=lang==='pt'?'pt-BR':'en';
  applyLang();
}
function applyLang(){
  const t=i18n[lang];
  document.querySelectorAll('[data-i18n]').forEach(el=>{const k=el.getAttribute('data-i18n');if(t[k]!==undefined)el.innerHTML=t[k];});
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el=>{const k=el.getAttribute('data-i18n-placeholder');if(t[k]!==undefined)el.placeholder=t[k];});
}
function toggleTheme(){
  theme=theme==='dark'?'light':'dark';
  document.documentElement.setAttribute('data-theme',theme);
  document.getElementById('themeBtn').textContent=theme==='dark'?'☀':'☾';
  document.querySelectorAll('img[data-icon]').forEach(img=>{
    img.src=`icon/${theme}/${img.getAttribute('data-icon')}.svg`;
  });
}
function toggleMenu(){
  document.getElementById('navLinks').classList.toggle('open');
  document.getElementById('hamburger').classList.toggle('open');
}
// ══ EMAILJS ══
emailjs.init('49WERftjvNxygg3C8');

function sendForm(){
  const btn     = document.querySelector('[data-i18n="form_btn"]');
  const name    = document.getElementById('form-name').value.trim();
  const email   = document.getElementById('form-email').value.trim();
  const subject = document.getElementById('form-subject').value.trim();
  const message = document.getElementById('form-message').value.trim();

  if(!name || !email || !subject || !message){
    alert(lang==='pt' ? 'Preencha todos os campos.' : 'Please fill in all fields.');
    return;
  }

  btn.textContent = lang==='pt' ? 'Enviando...' : 'Sending...';
  btn.disabled = true;

  emailjs.send('service_5vxay59', 'template_wcawyrb', {
    name      : name,
    user_email: email,
    subject   : subject,
    message   : message,
  })
  .then(() => {
    btn.textContent = lang==='pt' ? '✓ Mensagem enviada!' : '✓ Message sent!';
    btn.style.background = 'var(--accent2)';
    document.getElementById('form-name').value    = '';
    document.getElementById('form-email').value   = '';
    document.getElementById('form-subject').value = '';
    document.getElementById('form-message').value = '';
    setTimeout(() => {
      btn.textContent     = lang==='pt' ? 'Enviar Mensagem' : 'Send Message';
      btn.style.background = '';
      btn.disabled = false;
    }, 4000);
  })
  .catch(err => {
    console.error('EmailJS error:', err);
    btn.textContent  = lang==='pt' ? '✗ Erro ao enviar' : '✗ Failed to send';
    btn.style.background = '#c0392b';
    setTimeout(() => {
      btn.textContent     = lang==='pt' ? 'Enviar Mensagem' : 'Send Message';
      btn.style.background = '';
      btn.disabled = false;
    }, 4000);
  });
}

// Reveal
const obs=new IntersectionObserver(e=>{e.forEach(x=>{if(x.isIntersecting)x.target.classList.add('visible');});},{threshold:.1});
document.querySelectorAll('.reveal').forEach(el=>obs.observe(el));

// Active nav + scroll progress bar
const scrollBar=document.getElementById('scrollProgress');
window.addEventListener('scroll',()=>{
  let cur='';
  document.querySelectorAll('section[id]').forEach(s=>{if(window.scrollY>=s.offsetTop-130)cur=s.id;});
  document.querySelectorAll('.nav-links a').forEach(a=>{a.classList.toggle('active',a.getAttribute('href')==='#'+cur);});
  const max=document.documentElement.scrollHeight-window.innerHeight;
  if(scrollBar&&max>0)scrollBar.style.width=(window.scrollY/max*100)+'%';
});

// Close menu on link click
document.querySelectorAll('.nav-links a').forEach(a=>{
  a.addEventListener('click',()=>{document.getElementById('navLinks').classList.remove('open');document.getElementById('hamburger').classList.remove('open');});
});
